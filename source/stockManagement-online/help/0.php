<h1>Funktionen der einzelnen Fenster</h1>
<h2>Allgemeines</h2>
<img src="./images/image25.png" width="150px"></img>
<p>Dieser Button meldet den aktuellen Benutzer des Programms ab und wird in den meisten Fenstern angezeigt. Anschließend wird das 'Login Fenster’ (2.3) angezeigt.
Sie sollten sich nicht nur abmelden, wenn Sie mit ihrer Arbeit fertig sind, sondern auch, wenn Sie den Computer verlassen. Somit kann niemand mit Ihrem Account (und somit mit Ihrem Namen) Schaden anrichten.</p>

<img src="./images/image03.png" width="250px"></img>
<p>Um das Programm zu beenden, klicken sie einfach auf das 'X', welches sich am oberen, rechten Rand ihres Fensters befindet. Sie werden automatisch ausgeloggt und ihre Änderungen verworfen.</p>

<img src="./images/image28.png"></img>
<p>Dieser Button führt zum vorherigen Fenster zurück, wenn man ihn anklickt und bricht gegebenenfalls die aktuelle Aktion ab.
Pflichtfelder sind mit '*' gekennzeichnet. In Eingabefeldern mit diesem Zeichen MUSS eine Eingabe erfolgen. Sollten Pflichtfelder nicht ausgefüllt sein, kann man seine Eingaben/Änderungen nicht speichern! Sollten Sie es dennoch versuchen, so werden entsprechende Pflichtfelder rot markiert.</p>

<p>Nicht jeder Benutzer kann alle Funktionen des Programms nutzen. Fehlt Ihnen eine entsprechende Funktion, die in diesem Dokument erklärt wird, dann haben Sie nicht die Berechtigung diese zu nutzen.</p>

<img src="./images/image09.jpg" width="250px"></img>
<p>Das DRK Logo öffnet, wenn Sie es anklicken, Ihren Web Browser und öffnet dort einen neuen Tab mit der Internetseite des DRK Sennestadt.</p>

<p>Sollten die Listen/Bestände leer sein, so ist die Datenbank Verbindung unterbrochen worden und Sie müssen das Programm neu starten.</p>
