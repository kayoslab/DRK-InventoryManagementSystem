<img src="./images/image14.png"></img></br></br></br>
<p>In diesem Fenster werden die Login Daten eingetragen, die Sie von Ihrem Administrator erhalten haben. Füllen Sie die beiden Felder aus und drücken auf 'Einloggen'. Sind die Daten korrekt, gelangen Sie in das Hauptmenü. Wackelt der 'Einloggen' Button nach anklicken, so sind Ihre Login-Daten nicht korrekt.</p>
