<h1>Hauptmenü</h1>
<p>Nachdem die Daten für 'URL', 'Nutzername' und 'Passwort' eingetragen sind, klicken Sie auf 'speichern'. Das Programm prüft anschließend die Verbindung, speichert im Erfolgsfall die Daten in der Konfigurationsdatei in Ihrem Benutzerordner und öffnet das 'Login Fenster’ (2.3).
Sollte der Button 'speichern' wackeln, so ist momentan keine Verbindung zum Datenbankserver möglich. Wenden Sie sich in diesem Fall an Ihren zuständigen Administrator.</p>

<p>Von diesem Fenster aus erreichen Sie alle Funktionen, die das Programm beinhaltet. (Hinweis: Nicht jeder Benutzer kann alle Funktionen sehen/benutzen.)</p>

<p>Dieser Button öffnet das Fenster für 'Meldungen'.</p>

<p>Dies ist der ‘Objekt hinzufügen’ Button. Dieser öffnet das oben links zu sehende Popup, mit dem diverse Objekte neu angelegt werden können (2.10.1 bis 2.10.5). Diese Funktion steht generell nur Administratoren zur Verfügung.</p>

<p>Dies ist der ‘Objekt bearbeiten’ Button. Dieser öffnet das oben rechts zu sehende Popup, mit dem diverse bestehende Objekte bearbeitet werden können. Diese Funktion steht generell nur Administratoren zur Verfügung.</p>

<p>Dieser Button zeigt das Hilfe-Dokument.</p>

<p>Die großen Buttons in der Mitte des Fensters führen je zu den Fenstern für Material- und Geräte Daten, Inventarlisten und Einstellungen.</p>
