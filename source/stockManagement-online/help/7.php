<h1>Einstellungen</h1>
<img src="./images/image10.png"></img></br></br></br>
<p>In dem 'Einstellungen Fenster’ haben Sie die Möglichkeit die Buttons 'Datenbankzugangsdaten ändern’ oder 'Passwort ändern’ zu klicken. Ersteres steht in der Regel nur Administratoren zur Verfügung.</p>

<h2>Datenbankzugangsdaten ändern</h2>
<img src="./images/image30.png"></img></br></br></br>
<p>In diesem Fenster können Sie die Zugangsdaten zum Datenbankserver ändern. Dies ist in der Regel nicht nötig. Das oben stehende Beispiel zeigt, in welchem Format die Datenbank URL eingegeben werden muss. ( jdbc:mysql://[IP Adresse]:[Port]/[Datenbankname] )
Nutzername und Passwort entsprechen nicht den Zugangsdaten für dieses Programm.</p>

<h2>Passwort ändern</h2>
<img src="./images/image04.png"></img></br></br></br>
<p>In diesem Fenster kann das eigene Passwort geändert werden. Dazu müssen Sie in das oberste Feld Ihr aktuelles Passwort eingeben, in das mittlere Ihr neues Passwort und aus Sicherheitsgründen im dritten Feld Ihr Passwort wiederholen.
Ist das aktuelle Passwort nicht korrekt oder stimmen die beiden Eingaben des neuen Passworts nicht überein, scheitert der Vorgang. Dies wird durch einen wackelnden 'speichern' Button angezeigt.</p>
