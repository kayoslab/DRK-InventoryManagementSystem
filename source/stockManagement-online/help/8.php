<h1>Meldungen</h1>
<p>In diesem Fenster werden verschiedene Meldungen angezeigt, wie:
● Unterschreitung von Mindestbeständen
● Unterschreitung von Sollbeständen
● Überschreitung von Haltbarkeitsdaten
● Bald fällige Prüfzeiten der Geräte
● Überfällige Prüfzeiten der Geräte
Über den Radio Button können Sie zwischen den Reitern für Meldungen und Warnungen wechseln.</p>
