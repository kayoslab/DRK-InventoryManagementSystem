<h1>Meldungen</h1>
<p>In diesem Fenster werden verschiedene Meldungen angezeigt, wie:
<ul>
    <li>Unterschreitung von Mindestbeständen</li>
    <li>Unterschreitung von Sollbeständen</li>
    <li>Überschreitung von Haltbarkeitsdaten</li>
    <li>Bald fällige Prüfzeiten der Geräte</li>
    <li>Überfällige Prüfzeiten der Geräte</li>
</ul>
Über den Radio Button können Sie zwischen den Reitern für Meldungen und Warnungen wechseln.</p>
